package vlcUI;

public class DEV {
	private int id;
	private String ip ;  
	private String name;  
	private String status;  
	public String getIP() {  
	    return ip;  
	}  
	public void setIP(String ip) {  
	    this.ip = ip;  
	}  
	public String getName() {  
	    return name;  
	}  
	public void setName(String name) {  
	    this.name =name;  
	}  
	public String getStatus() {  
	    return status;  
	}  
	public void setStatus(String status) {  
	    this.status = status;  
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	} 
}
